package passatempo.condominio;

public class Condomino extends Persona{

	private Boolean capofamiglia;
	private String recapito;
	private String inizioPermanenza;
	public Boolean getCapofamiglia() {
		return capofamiglia;
	}
	public void setCapofamiglia(Boolean capofamiglia) {
		this.capofamiglia = capofamiglia;
	}
	public String getRecapito() {
		return recapito;
	}
	public void setRecapito(String recapito) {
		this.recapito = recapito;
	}
	public String getInizioPermanenza() {
		return inizioPermanenza;
	}
	public void setInizioPermanenza(String inizioPermanenza) {
		this.inizioPermanenza = inizioPermanenza;
	}
	

	
}
